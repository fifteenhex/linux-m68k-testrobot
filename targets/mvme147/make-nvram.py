#!/usr/bin/env python3
"""Generate a 147Bug-valid MK48T02 NVRAM image for the QEMU mvme147 machine.

Usage: make-nvram.py [--romboot] [nvram.img]
Attach with: -drive if=mtd,file=nvram.img,format=raw

The contents were produced by 147Bug 2.5 itself (boot + ENV dialog) plus
the config flags its boot-time NVRAM validator requires, so a cold boot
comes up with no "Unreliable R/W to NVRAM" warning.

--romboot enables the power-up ROM boot scan (byte 0x7f2='Y', 0x7f4='R')
so 147Bug automatically finds and runs a "BOOT" module in ROM (see
build-romboot.py) instead of needing a manual bm/go.
"""
