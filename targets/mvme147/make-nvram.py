args = sys.argv[1:]
romboot = "--romboot" in args
args = [a for a in args if a != "--romboot"]
out = args[0] if args else "nvram.img"

img = bytearray(base64.b64decode(IMAGE))
if romboot:
    img[0x7f2] = ord("Y")  # ROM boot enable at power-up
    img[0x7f4] = ord("R")  # ROM boot search enable

open(out, "wb").write(bytes(img))
print(f"wrote {out}" + (" (ROM boot enabled)" if romboot else ""))
